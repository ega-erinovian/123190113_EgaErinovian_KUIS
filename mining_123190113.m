function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
training = xlsread('zoo.xls');
group = xlsread('group.xls');
classification = fitcknn(training, group, 'NumNeighbors', handles.k);
sample = [handles.hair handles.feathers handles.eggs handles.milk handles.airborne handles.aquatic handles.predator handles.toothed handles.backbone handles.breathes handles.venomous handles.fins handles.legs handles.tail handles.domestic handles.catsize];
