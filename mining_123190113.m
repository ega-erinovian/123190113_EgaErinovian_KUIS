if out == 1
    class = 'Mammals';
elseif out == 2
    class = 'Bird';
elseif out == 3
    class = 'Reptile';
elseif out == 4
    class = 'Fish';
elseif out == 5
    class = 'Amphibian';
elseif out == 6
    class = 'Bug';
elseif out == 7
    class = 'Invertebrate';
end
set(handles.edit18,'string',class);

